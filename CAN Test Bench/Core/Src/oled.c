void oled_Write_Cmd( unsigned char cmd )
{
//    ... // make PB6 = CS# = 1
	GPIOB->BSRR = 0x1<<6;

//    ... // make PB7 = D/C# = 0
	GPIOB->BSRR = 0x1<<23;

//    ... // make PB6 = CS# = 0
	GPIOB->BSRR = 0x1<<22;

    oled_Write( cmd );

//    ... // make PB6 = CS# = 1
    GPIOB->BSRR = 0x1<<6;
}

void oled_Write_Data( unsigned char data )
{
    //... // make PB6 = CS# = 1
	GPIOB->BSRR = 0x1<<6;

    //... // make PB7 = D/C# = 1
	GPIOB->BSRR = 0x1<<7;

    //... // make PB6 = CS# = 0
	GPIOB->BSRR = 0x1<<22;

    oled_Write( data );

    //... // make PB6 = CS# = 1
    GPIOB->BSRR = 0x1<<6;
}


void oled_Write( unsigned char Value )
{

    /* Wait until SPI1 is ready for writing (TXE = 1 in SPI1_SR) */

   while((SPI1->SR & SPI_SR_TXE) == 0 ){}

    /* Send one 8-bit character:
       - This function also sets BIDIOE = 1 in SPI1_CR1
    */
    HAL_SPI_Transmit( &SPI_Handle, &Value, 1, HAL_MAX_DELAY );

    /* Wait until transmission is complete (TXE = 1 in SPI1_SR) */

    while((SPI1->SR & SPI_SR_TXE) == 0 ){}

}


void oled_config( void ){

// Don't forget to enable GPIOB clock in RCC - Done
// Don't forget to configure PB3/PB5 as AF0 - Done
// Don't forget to enable SPI1 clock in RCC - Done

	RCC->APB2ENR |= 0x1000; //enable SPI1 clock

    SPI_Handle.Instance = SPI1;

    SPI_Handle.Init.Direction = SPI_DIRECTION_1LINE;
    SPI_Handle.Init.Mode = SPI_MODE_MASTER;
    SPI_Handle.Init.DataSize = SPI_DATASIZE_8BIT;
    SPI_Handle.Init.CLKPolarity = SPI_POLARITY_LOW;
    SPI_Handle.Init.CLKPhase = SPI_PHASE_1EDGE;
    SPI_Handle.Init.NSS = SPI_NSS_SOFT;
    SPI_Handle.Init.BaudRatePrescaler = SPI_BAUDRATEPRESCALER_256;
    SPI_Handle.Init.FirstBit = SPI_FIRSTBIT_MSB;
    SPI_Handle.Init.CRCPolynomial = 7;

//
// Initialize the SPI interface
//
    HAL_SPI_Init( &SPI_Handle );

//
// Enable the SPI
//
    __HAL_SPI_ENABLE( &SPI_Handle );


    /* Reset LED Display (RES# = PB4):
       - make pin PB4 = 0, wait for a few ms
       - make pin PB4 = 1, wait for a few ms
    */
    //...
    GPIOB->BSRR = 0x1<<20;
    wait(10);
    GPIOB->BSRR = 0x1<<4;


//
// Send initialization commands to LED Display
//
    for ( unsigned int i = 0; i < sizeof( oled_init_cmds ); i++ ){
        oled_Write_Cmd( oled_init_cmds[i] );
    }

    /* Fill LED Display data memory (GDDRAM) with zeros:
       - for each PAGE = 0, 1, ..., 7
           set starting SEG = 0
           call oled_Write_Data( 0x00 ) 128 times
    */

    for (unsigned int i = 0;  i<8; i++){
    	oled_Write_Cmd(0xB0 | ((unsigned char)i));
    	oled_Write_Cmd(0x02);
    	oled_Write_Cmd(0x10);

    	for(unsigned int j = 0; j<128; j++){

    		oled_Write_Data(0x00);
    	}

    }



}

